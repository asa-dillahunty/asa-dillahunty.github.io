export const getPolyMarketSlugFromUrl = (url: string) => {
  const segments = url.split("/");
  const slug = segments[segments.length - 1];
  return slug;
};

export const getEventBySlug = async (slug: string) => {
  const options = { method: "GET" };

  const response = await fetch(
    // `http://127.0.0.1:5001/letterboxedsolver/us-central1/api/api/polymarket/${slug}`,
    `https://letterboxed.asadillahunty.com/api/polymarket/${slug}`,
    options,
  );

  const event = await response.json();
  // const event = testingEventData;
  const filteredEvent = filterEventData(event);

  return filteredEvent;
};

const filterEventData = (eventData: PolymarketEvent) => {
  const newEvent = {
    title: eventData.title,
    description: eventData.description,
    image: eventData.image,
    slug: eventData.slug,
    markets: filterMarkets(eventData.markets),
  };
  return newEvent;
};

const filterMarkets = (markets: Market[]) => {
  const filteredMarkets = markets
    .filter((m) => m.active === true && m.closed === false)
    .map((m) => ({
      groupItemTitle: m.groupItemTitle,
      lastTradePrice: m.lastTradePrice,
      bestBid: m.bestBid ?? m.lastTradePrice, // sometimes these values can be undefined and firebase doesn't allow that
      bestAsk: m.bestAsk ?? m.lastTradePrice,
      slug: m.slug,
    }));

  return filteredMarkets;
};

